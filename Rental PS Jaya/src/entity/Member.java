package entity;

/**
 *
 * @author LENOVO
 */
public class Member {
    private int id_member;
    private String nama;
    private int no_telepon;
    private String alamat;

    /**
     * @return the id_member
     */
    public int getId_member() {
        return id_member;
    }

    /**
     * @param id_member the id_member to set
     */
    public void setId_member(int id_member) {
        this.id_member = id_member;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the no_telepon
     */
    public int getNo_telepon() {
        return no_telepon;
    }

    /**
     * @param no_telepon the no_telepon to set
     */
    public void setNo_telepon(int no_telepon) {
        this.no_telepon = no_telepon;
    }

    /**
     * @return the alamat
     */
    public String getAlamat() {
        return alamat;
    }

    /**
     * @param alamat the alamat to set
     */
    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
 
}
