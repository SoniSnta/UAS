package entity;

/**
 *
 * @author LENOVO
 */
public class Pegawai {
    private int pegawai;
    private String nama_lengkap;
    private int no_telepon;
    private String jenis_kelamin;

    /**
     * @return the pegawai
     */
    public int getPegawai() {
        return pegawai;
    }

    /**
     * @param pegawai the pegawai to set
     */
    public void setPegawai(int pegawai) {
        this.pegawai = pegawai;
    }

    /**
     * @return the nama_lengkap
     */
    public String getNama_lengkap() {
        return nama_lengkap;
    }

    /**
     * @param nama_lengkap the nama_lengkap to set
     */
    public void setNama_lengkap(String nama_lengkap) {
        this.nama_lengkap = nama_lengkap;
    }

    /**
     * @return the no_telepon
     */
    public int getNo_telepon() {
        return no_telepon;
    }

    /**
     * @param no_telepon the no_telepon to set
     */
    public void setNo_telepon(int no_telepon) {
        this.no_telepon = no_telepon;
    }

    /**
     * @return the jenis_kelamin
     */
    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    /**
     * @param jenis_kelamin the jenis_kelamin to set
     */
    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }
}
